
public class positive_negativeNumber {

    public static void main(String[] args) {
        int num = 0;
        if (num > 0) {
            System.out.println(num + " is a postive number");
        } else if (num == 0) {
            System.out.println(num + " number is a zero");
        } else {
            System.out.println(num + " is a negative number");
        }
    }

}
