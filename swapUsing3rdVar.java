public class swapUsing3rdVar {
    public static void main(String[] args) {
        int a=10;
        int b=60;
        int temp;

        System.out.println("before swap");
        System.out.println("a = "+a +" "+"b = "+b);

          System.out.println("after swap");
          temp=a;
          a=b;
          b=temp;

        System.out.println("a = "+a +" "+"b = "+b);

    }
    
}
